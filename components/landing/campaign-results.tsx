function CompareSlider({
  before,
  after,
  alt,
}: {
  before: string;
  after: string;
  alt: string;
}) {
  const [sliderPosition, setSliderPosition] = useState(50);
  const containerRef = useRef<HTMLDivElement>(null);
  const isDragging = useRef(false);

  const handleMove = (
    event: React.MouseEvent | React.TouchEvent | MouseEvent | TouchEvent
  ) => {
    if (!containerRef.current) return;

    const containerRect = containerRef.current.getBoundingClientRect();
    let clientX;

    if ("touches" in event) {
      clientX = event.touches[0].clientX;
    } else {
      clientX = (event as MouseEvent).clientX;
    }

    const x = Math.max(
      0,
      Math.min(clientX - containerRect.left, containerRect.width)
    );
    const percentage = (x / containerRect.width) * 100;

    setSliderPosition(percentage);
  };

  const handleMouseDown = () => {
    isDragging.current = true;
  };

  const handleMouseUp = () => {
    isDragging.current = false;
  };

  useEffect(() => {
    const handleGlobalMove = (e: MouseEvent | TouchEvent) => {
      if (isDragging.current) {
        handleMove(e);
      }
    };

    const handleGlobalUp = () => {
      isDragging.current = false;
    };

    window.addEventListener("mousemove", handleGlobalMove);
    window.addEventListener("mouseup", handleGlobalUp);
    window.addEventListener("touchmove", handleGlobalMove);
    window.addEventListener("touchend", handleGlobalUp);

    return () => {
      window.removeEventListener("mousemove", handleGlobalMove);
      window.removeEventListener("mouseup", handleGlobalUp);
      window.removeEventListener("touchmove", handleGlobalMove);
      window.removeEventListener("touchend", handleGlobalUp);
    };
  }, []);

  return (
    <div
      ref={containerRef}
      className="relative h-[400px] overflow-hidden cursor-ew-resize select-none border-4 border-emerald"
      onMouseDown={handleMouseDown}
      onTouchStart={handleMouseDown}
    >
      <div className="absolute inset-0 bg-black flex items-center justify-center">
        {/* Simulating "After" State - High Growth */}
        <div className="text-center">
          <TrendingUp className="w-24 h-24 text-emerald-500 mx-auto mb-4" />
          <h3 className="text-4xl font-bold text-white">VIRAL GROWTH</h3>
          <p className="text-emerald-500 text-xl">After Lumina Clippers</p>
        </div>
      </div>
      <div
        className="absolute inset-0 bg-neutral-900 flex items-center justify-center"
        style={{ clipPath: `inset(0 ${100 - sliderPosition}% 0 0)` }}
      >
        {/* Simulating "Before" State - Low Growth */}
        <div className="text-center opacity-50">
          <TrendingUp className="w-24 h-24 text-gray-500 mx-auto mb-4 rotate-180" />
          <h3 className="text-4xl font-bold text-gray-400">STAGNANT</h3>
          <p className="text-gray-500 text-xl">Before Lumina Clippers</p>
        </div>
      </div>
      <div
        className="absolute top-0 bottom-0 w-1 bg-emerald-500 cursor-ew-resize"
        style={{ left: `${sliderPosition}%` }}
      >
        <div className="absolute top-1/2 left-1/2 -translate-x-1/2 -translate-y-1/2 w-12 h-12 bg-emerald-500 border-4 border-black flex items-center justify-center shadow-lg">
          <div className="flex gap-1">
            <div className="w-0.5 h-6 bg-black"></div>
            <div className="w-0.5 h-6 bg-black"></div>
          </div>
        </div>
      </div>
    </div>
  );
}

